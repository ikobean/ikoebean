package com.ch7;

public class Product_detail {
	
  private String color               = null; //
  private String image               = null; //
  private String ecer                = null; //
  private String pro_size            = null; //
  private String model_no            = null; //
  private String nation              = null; //
  private String manufacture_date    = null; //
  private int pro_code               = 0; //
      
  
public String getColor() {
	return color;
}
public void setColor(String color) {
	this.color = color;
}
public String getImage() {
	return image;
}
public void setImage(String image) {
	this.image = image;
}
public String getEcer() {
	return ecer;
}
public void setEcer(String ecer) {
	this.ecer = ecer;
}
public String getPro_size() {
	return pro_size;
}
public void setPro_size(String pro_size) {
	this.pro_size = pro_size;
}
public String getModel_no() {
	return model_no;
}
public void setModel_no(String model_no) {
	this.model_no = model_no;
}
public String getNation() {
	return nation;
}
public void setNation(String nation) {
	this.nation = nation;
}
public String getManufacture_date() {
	return manufacture_date;
}
public void setManufacture_date(String manufacture_date) {
	this.manufacture_date = manufacture_date;
}
public int getPro_code() {
	return pro_code;
}
public void setPro_code(int pro_code) {
	this.pro_code = pro_code;
}
                                             
}
