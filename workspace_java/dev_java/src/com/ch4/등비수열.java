package com.ch4;

public class 등비수열 {
	
public static void main(String[] args) {
//등비수열의 항에 대응하는 값을 저장할 변수 선언
  int j=2;
  int i;
  for(i=1;i<=6;i++) {
/*printf함수를 사용하면 출력되는 값에 자리수를 정해 줄 수 있음
* %5에서 숫자 5가 자리수를 나타냄.
* d는10진수를 말함.
* 만일 문자열이면 %s, character는 %c
*/
   System.out.println(j);
   j*=3;//j=j*3  
    
  }
}
}