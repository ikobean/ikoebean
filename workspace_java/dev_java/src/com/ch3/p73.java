package com.ch3;

public class p73 {
	/*
	 * int + int = int
	 * int - double = double
	 * double * int = double
	 * int/float = float
	 */

	public static void main(String[] args) {
        System.out.println(2+4); //6
        System.out.println(2-4); //-2
        System.out.println(2*4.0); //8
        System.out.println(2/4.0); //0 
        System.out.println(2%4); //2
               
        
        
        
        
	}

}
