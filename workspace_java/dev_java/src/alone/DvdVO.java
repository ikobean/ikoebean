package alone;

public class DvdVO {

	private String dvd_title=null;
	private int dvd_price=0;
	private String dvd_dir=null;
	
	public String getDvd_title() {
		return dvd_title;
	}
	public void setDvd_title(String dvd_title) {
		this.dvd_title = dvd_title;
	}
	public int getDvd_price() {
		return dvd_price;
	}
	public void setDvd_price(int dvd_price) {
		this.dvd_price = dvd_price;
	}
	public String getDvd_dir() {
		return dvd_dir;
	}
	public void setDvd_dir(String dvd_dir) {
		this.dvd_dir = dvd_dir;
	}
	
	
}
